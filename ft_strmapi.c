#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*ptr;
	size_t			i;
	size_t			len;

	if (!s || !f)
		return (NULL);
	len = ft_strlen((const char*)s);
	if (!(ptr = ft_strnew(len)))
		return (NULL);
	i = 0;
	while (i < len)
	{
		ptr[i] = f(i, s[i]);
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
